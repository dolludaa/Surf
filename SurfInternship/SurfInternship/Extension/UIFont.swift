import Foundation
import UIKit

extension UIFont {
    static func sfProDisplayRegular(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "SFProDisplay-Regular", size: size)!
    }

    static func sf24_32_Bold(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "24_32_Bold SF", size: size)!
    }
}
